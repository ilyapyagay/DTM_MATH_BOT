async def get_user(telegram_id: int) -> User:
    async with async_session() as session:
        result = await session.execute(select(User).where(User.telegram_id == telegram_id))
        user = result.scalar_one_or_none()
        if not user:
            user = User(telegram_id=telegram_id, has_access=False)
            session.add(user)
            await session.commit()
            await session.refresh(user)
        return user


async def grant_access(telegram_id: int, username: str = None):
    async with async_session() as session:
        result = await session.execute(select(User).where(User.telegram_id == telegram_id))
        user = result.scalar_one_or_none()
        if user:
            user.has_access = True
            user.username = username
        else:
            user = User(telegram_id=telegram_id, username=username, has_access=True)
            session.add(user)
        await session.commit()


async def get_random_question() -> Question:
    """Получить случайный вопрос (устаревшая функция)"""
    async with async_session() as session:
        result = await session.execute(select(Question).order_by(Question.id))
        questions = result.scalars().all()
        if questions:
            import random
            return random.choice(questions)
        return None


async def get_next_question_for_user(telegram_id: int) -> Question:
    """Получить следующий вопрос для пользователя по кругу"""
    async with async_session() as session:
        user_result = await session.execute(
            select(User).where(User.telegram_id == telegram_id)
        )
        user = user_result.scalar_one_or_none()
        
        questions_result = await session.execute(
            select(Question).order_by(Question.id)
        )
        questions = questions_result.scalars().all()
        
        if not questions:
            return None
        
        if not user or not user.current_question_id:
            return questions[0]
        
        current_index = None
        for i, q in enumerate(questions):
            if q.id == user.current_question_id:
                current_index = i
                break
        
        if current_index is not None:
            next_index = (current_index + 1) % len(questions)
            return questions[next_index]
        else:
            return questions[0]


