def clean_explanation_text(text: str) -> str:
    """Удаляет символы заголовков Markdown (###, ####) и лишние артефакты форматирования для Telegram"""
    if not text:
        return ""
    
    lines = []
    for line in text.split('\n'):
        stripped = line.lstrip()
        # Если строка начинается с решеток (например: "### **Шаг 1**")
        if re.match(r'^#{1,6}\s*', stripped):
            cleaned_line = re.sub(r'^#{1,6}\s*', '', stripped).strip()
            if cleaned_line and not any(cleaned_line.startswith(emoji) for emoji in ['📌', '✏️', '⚡', '💡', '🔍', '🔢', '✅', '🏆', '💖', '✨']):
                cleaned_line = f"📌 {cleaned_line}"
            lines.append(cleaned_line)
        else:
            lines.append(line)
            
    result = '\n'.join(lines)
    result = re.sub(r'\*\*\s*\*\*', '', result)
    return result.strip()


async def explain_solution(question_text: str, correct_answer: str,
                           option_a: str, option_b: str, option_c: str, option_d: str) -> str:

    user_prompt = f"""
📝 ЗАДАЧА ДЛЯ ВИКУЛИ:
{question_text}

ВАРИАНТЫ ОТВЕТА:
A) {option_a}
B) {option_b}
C) {option_c}
D) {option_d}

ПРАВИЛЬНЫЙ ОТВЕТ: {correct_answer}

Тепло обратись к Викуле и объясни решение подробно и понятно по шагам без символов решеток (#).
"""

    try:
        response = model.generate_content(
            f"{SYSTEM_PROMPT}\n\n{user_prompt}"
        )
        return clean_explanation_text(response.text)

    except Exception as e:
        return f"❌ Ошибка при обращении к ИИ: {str(e)}\n\nПопробуйте позже или обратитесь к администратору."
