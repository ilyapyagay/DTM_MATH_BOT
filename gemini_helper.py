import re
import google.generativeai as genai
from config import GEMINI_API_KEY

genai.configure(api_key=GEMINI_API_KEY)
model = genai.GenerativeModel('gemini-2.5-flash')

SYSTEM_PROMPT = """Ты — опытный репетитор по математике с 15-летним стажем подготовки к экзаменам DTM (УзБМБ) в Узбекистане.

Твоя задача — объяснить решение математической задачи так, чтобы абитуриент не просто узнал правильный ответ, а ПОНЯЛ принцип решения и смог решить похожие задачи самостоятельно.

ОГРАНИЧЕНИЯ ПО ФОРМАТИРОВАНИЮ (КРИТИЧЕСКИ ВАЖНО!):
- КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО использовать символы решетки (`#`, `##`, `###`, `####`) для создания заголовков! В Telegram они выглядят как грязные артефакты.
- Не используй странные комбинации вроде `### **` или `** **`.
- Для оформления пунктов и шагов используй эмодзи и аккуратное жирное выделение.

СТРУКТУРА ТВОЕГО ОБЪЯСНЕНИЯ:

📌 **Шаг 1: Что дано и что нужно найти**
Перечисли исходные данные простыми словами и сформулируй цель задачи.

✏️ **Шаг 2: Какую формулу или правило использовать**
Назови математическое правило/формулу и объясни, почему именно оно подходит.

🔢 **Шаг 3: Пошаговые вычисления**
Покажи подстановку чисел в формулу и подробно опиши каждое промежуточное действие.

✅ **Шаг 4: Ответ и проверка**
Проверь логику результата и укажи правильный вариант ответа.

💡 **Лайфхак для экзамена УзБМБ**
Дай короткий совет, как решить такую задачу быстрее или избежать типичных ошибок.

ПРАВИЛА ОФОРМЛЕНИЯ:
- Пиши на языке вопроса (русский).
- Формулы пиши в моноширинном коде: `x = 5`.
- Используй математический Unicode: x², x³, √, ≤, ≥, ±, ×, ÷.
- Будь дружелюбным и подбадривающим!"""


def clean_explanation_text(text: str) -> str:
    """Удаляет символы заголовков Markdown (###, ####) и лишние артефакты форматирования для Telegram"""
    if not text:
        return ""
    
    lines = []
    for line in text.split('\n'):
        stripped = line.lstrip()
        # Если строка начинается с решеток (например: "### **Шаг 1**")
        if re.match(r'^#{1,6}\s*', stripped):
            cleaned_line = re.sub(r'^#{1,6}\s*', '', stripped).strip()
            # Если строка не имеет красивого эмодзи в начале, добавим булавку
            if cleaned_line and not any(cleaned_line.startswith(emoji) for emoji in ['📌', '✏️', '⚡', '💡', '🔍', '🔢', '✅', '🏆']):
                cleaned_line = f"📌 {cleaned_line}"
            lines.append(cleaned_line)
        else:
            lines.append(line)
            
    result = '\n'.join(lines)
    # Очищаем двойные звездочки без текста внутри и лишние пробелы
    result = re.sub(r'\*\*\s*\*\*', '', result)
    return result.strip()


async def explain_solution(question_text: str, correct_answer: str,
                           option_a: str, option_b: str, option_c: str, option_d: str) -> str:

    user_prompt = f"""
📝 ЗАДАЧА:
{question_text}

ВАРИАНТЫ ОТВЕТА:
A) {option_a}
B) {option_b}
C) {option_c}
D) {option_d}

ПРАВИЛЬНЫЙ ОТВЕТ: {correct_answer}

Объясни решение подробно и понятно по 5 шагам без символов решеток (#).
"""

    try:
        response = model.generate_content(
            f"{SYSTEM_PROMPT}\n\n{user_prompt}"
        )
        return clean_explanation_text(response.text)

    except Exception as e:
        return f"❌ Ошибка при обращении к ИИ: {str(e)}\n\nПопробуйте позже или обратитесь к администратору."
